package helpers

// 1.1. Write a function printNumbers that prints numbers from 1 to 10.
func PrintNumbers() {
	for i := 1; i <= 10; i++ {
		println(i)
	}
}

// 1.2. Write a function printLetters that prints letters from 'a' to 'j'.
func PrintLetters() {
	for i := 'a'; i <= 'j'; i++ {
		println(string(i))
	}
}
